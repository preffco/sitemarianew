// Функция для выделения фраз жирным
function highlightBold(text: string) {
  const phrases = [
    'ИИ‑то у нас внедрен. Людей нет, которые бы умели его использовать в компании',
    'Почему сотрудники воспринимают ИИ как угрозу или лишнюю головную боль?',
    'Нет мотивации',
    'Ловушка операционки: «Мне некогда точить топор, я рублю лес»',
    'Отсутствие понятных правил работы с ИИ и страх ошибки',
    'Технологический скепсис: конфликт «Excel vs ИИ»',
    'Обучение «для галочки» или почему массовые онлайн‑курсы по ИИ не дают нужного результата',
    'Стратегия, которая работает: от теории к реальным задачам',
    'Создание безопасной среды для работы с ИИ',
    'Мастер‑классы на реальных задачах',
    'Примеры из практики',
    'Заключение'
  ]
  
  let result = text
  phrases.forEach(phrase => {
    const regex = new RegExp(phrase.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g')
    result = result.replace(regex, `<strong>${phrase}</strong>`)
  })
  
  return result
}

