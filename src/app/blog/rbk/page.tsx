// Функция для выделения фраз жирным
function highlightBold(text: string) {
  const phrases = [
    'ИИ-агент — это полностью проактивная автономная система',
    'ИИ-ассистент с агентским функционалом — реактивный инструмент, выполняющий команды пользователя и частично способный к автономным действиям. Например, он может найти и забронировать тур в Париж, но не оплатит его без подтверждения.',
    'Азиатская модель: от мессенджера к супераппу',
    'Российский контекст: гонка экосистем в обход мессенджеров',
    'Отдельно стоит рассмотреть Telegram',
    'Фактор «MAX»: создание супераппа «сверху вниз».',
    'Два сценария для российского рынка',
    'Сценарий 1: оптимистичный — создание локального супераппа.',
    'Сценарий 2: пессимистичный — отставание и нишевое использование.',
    'Итог этого противостояния между органическим развитием, государственной инициативой и инерцией рынка определит, станут ли ИИ-агенты в России настоящей революцией в пользовательском опыте или так и останутся нишевым инструментом.'
  ]
  
  let result = text
  phrases.forEach(phrase => {
    const regex = new RegExp(phrase.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g')
    result = result.replace(regex, `<strong>${phrase}</strong>`)
  })
  
  return result
}

